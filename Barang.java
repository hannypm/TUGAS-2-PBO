package tugas02;

public class Barang {
    private int noMinuman;
    private int stokBarang;
    private int noMakanan;


    public int getNoMakanan() {
        return noMakanan;
    }

    public void setNoMakanan(int noMakanan) {
        this.noMakanan = noMakanan;
    }

    public int getNoMinuman() {
        return noMinuman;
    }

    public void setNoMinuman(int noMinuman) {
        this.noMinuman = noMinuman;
    }

    public int getStokBarang() {
        return stokBarang;
    }

    public void setStokBarang(int stokBarang) {
        this.stokBarang = stokBarang;
    }


}
